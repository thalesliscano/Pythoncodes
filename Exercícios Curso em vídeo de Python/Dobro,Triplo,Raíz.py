#Aqui tive dificuldade em fazer a raiz, mas dai eu compreendi que pra calcular esse tipo de coisa temos que transformar a potêncai em fração, pra dair achar seu valor em ponto flutuante(float)

# Declarando variáveis
num = int(input("Digite um número: "))

# Resolvendo os problemas 
dobro = num * 2
triplo = num * 3
raiz  = num ** (1/2)

# Imprimindo o resultado de acordo com a saída desejada.
print("O dobro é {}".format(dobro))
print("O triplo é {}".format(triplo))
print("A raíz quadrada é {}".format(raiz))
