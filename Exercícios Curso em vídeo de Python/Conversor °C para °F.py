# Converte Celsius para Fahrenheit temperatura

# Declarando variáveis 
GrauCelsius_inicial = input()

# Convertendo variáveis
temperatura_convertida = float(GrauCelsius_inicial)

# Resolvendo conta
conversao = (temperatura_convertida * 1.8) + 32

#imprimindo o resultado de acordo com a saída desejada.
print("O valor convertido em Celsius para Grau Fahrenheit é {:.2f}".format(conversao))