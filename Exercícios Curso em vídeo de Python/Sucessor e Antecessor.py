# Exercício pra praticar um pouco mais a questão dos operadores aritméticos, só que forçando um pouco mais a lógica.

# Declarando variáveis
escreva = int(input("Digite um número: "))

# Resolvendo os problemas 
antecessor = escreva - 1
sucessor = escreva + 1

# Imprimindo os problemas
print("A variável escreva prossuí como antecessor {} e sucessor {}".format(antecessor, sucessor))