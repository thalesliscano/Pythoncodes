# Declarando variáveis 
sequencia = input("Digite algo: ")

# Subsequência1
subsequencia_um = input()

# Subsequência2
subsequencia_dois = input()

# Subsequência3
subsequencia_tres = input()

# Calcule o número de vezes que a subseq aparece.
numSequenciaUm = sequencia.count(subsequencia_um)
